def deck():
    suits = ["черви", "бубны", "пики", "трефы"]
    ranks = range(
        2, 15
    )  # Ранги от 2 до 14 включительно (где 11 — валет, 12 — дама, 13 — король, 14 — туз)

    for suit in suits:
        for rank in ranks:
            yield (rank, suit)


cards = list(deck())
