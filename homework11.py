# Задача: Анализ текстовых данных с использованием функционального подхода
#
# Вам необходимо разработать программу для анализа текстовых данных из нескольких файлов. Каждый файл содержит
# текстовую информацию о продажах определенного товара в разные периоды времени. Вам нужно выполнить следующие шаги с
# использованием функционального подхода:
#
# 1. Чтение данных:
#    Напишите функцию read_file(file_path), которая принимает путь к файлу и возвращает содержимое файла в виде списка
#    строк.
#
# 2. Обработка данных:
#    Напишите функцию process_data(data), которая принимает список строк с данными о продажах и преобразует их в
#    удобный формат (например, словарь с информацией о продажах для каждого периода времени).
#
# 3. Фильтрация данных:
#    Напишите функцию filter_data(data, threshold), которая принимает обработанные данные и пороговое значение
#    threshold для фильтрации. Функция должна вернуть только те записи о продажах, у которых объем продаж превышает
#    threshold.
#
# 4. Агрегация данных:
#    Напишите функцию aggregate_data(data), которая принимает отфильтрованные данные и агрегирует их, например,
#    вычисляя суммарный объем продаж по всем периодам времени.
#
# 5. Визуализация результатов:
#    Напишите функцию visualize_data(aggregated_data), которая принимает агрегированные данные и визуализирует их,
#    например, строит график или диаграмму.
#
# 6. Общий процесс:
#    Напишите основную функцию, которая объединяет все вышеперечисленные шаги и применяет их ко всем файлам с данными
#    о продажах.
#
# Эта задача поможет вам попрактиковаться в использовании функциональных подходов к обработке данных на Python, таких
# как отображение, фильтрация и свертка, а также в работе с файлами, структурами данных и визуализацией результатов.
#
# PS: Для работы с фалами используем open(). Не забываем закрывать файл в конце.
